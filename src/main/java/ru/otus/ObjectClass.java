package ru.otus;

public class ObjectClass {
    private String sField;
    private int iField;

    public ObjectClass() {
    }

    public String getSField() {
        return sField;
    }

    public void setSField(String field) {
        this.sField = field;
    }

    public int getIField() {
        return iField;
    }

    public void setIField(int field) {
        this.iField = field;
    }
}
